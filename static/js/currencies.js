$(document).ready(function () {
    $(document).on("change", "#id_country", function () {
        var country_symbol = $(this).val();

        function getValue(key, array) {
            for (var el in array) {
                if (array[el].hasOwnProperty(key)) {
                    return array[el][key];
                }
            }
        }

        var val = getValue(country_symbol, currencies);

        $('#id_currency').val(val.currency);
        $('#id_code').val(val.code);
        $('#id_symbol').val(val.symbol);

    });
});
